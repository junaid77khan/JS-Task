// Define the function
function tellFortune(numChildren, partnerName, location, jobTitle) {
  console.log(`You will be a ${jobTitle} in ${location}, and married to ${partnerName} with ${numChildren} kids.`);
}

// Call the function 3 times with different arguments
tellFortune(3, "Alex", "Canada", "Software Engineer");
tellFortune(2, "Taylor", "Japan", "Graphic Designer");
tellFortune(4, "Jordan", "Brazil", "Chef");